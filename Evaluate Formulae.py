# Given four inputs that are stored in variables a, b, c, and d. 
# You need to write an expression to evaluate the following formula. 
# Use integer division. 
# The expression should be a single statement.
# a+b/c + d

class Solution:
    def calculate(self, a: int, b: int, c: int, d: int) -> int:
        return (a + b) // c + d




